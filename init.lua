local function dfver(s) for _,v in ipairs(df) do if s:find(v) then return v end end end

minetest.register_on_joinplayer(function(player)
	local name = player:get_player_name()
	local dfv = dfver(minetest.get_player_information(name).version_string)
	if dfv then
		minetest.kick_player(name, "Unsupported client")
		if beerchat then
			beerchat.send_on_channel("DF Test", beerchat.moderator_channel_name, "Unsupported client removed: " .. dfv)
		end
	end
end)
